       <li>Physics</li>
       <li>Chemistry</li>
       <li>General Science</li>
       <li>History</li>
       <li>Geography</li>
       <li>Current Events</li>
       <li>Social Studies</li>
       <li>Economics</li>
       <li>Political Science</li>
      </ul>
