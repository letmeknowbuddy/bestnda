      <ul>
       <li>Algebra</li>
       <li>Matrices and Determinants</li>
       <li>Trigonometry</li>
       <li>Analytical Geometry of Two and Three Dimensions</li>
       <li>Differential Calculus</li>
       <li>Integral Calculus and Differential Equations</li>
       <li>Vector Algebra</li>
       <li>Statistics and Probability</li>
      </ul>
      <h3>2. General Ability Test (GAT): The GAT is further divided into two sections: English and General Knowledge.</h3>
      <h4>English:</h4>
      <ul class="dtl_list">
       <li>Vocabulary</li>
       <li>Grammar and Usage</li>
       <li>Comprehension and Cohesion</li>
       <li>Spotting Errors</li>
       <li>Sentence Improvement</li>
       <li>Ordering of Words/ Sentences</li>
       <li>Synonyms and Antonyms</li>
      </ul>
